print("Calculator")
def calculator():
  val = eval(input("enter the first value : "))
  operator = input("enter the operator : ")
  sign = operator
  val2 = eval(input("enter the second value : "))
  print("\nEntered value 1 = ",val)
  print("Entered operator = ",operator)
  print("Entered value 2 = ",val2)
  print("\n")
  if sign == '+' or sign == '-' or sign == '*' or sign == '×' or sign == '/' or sign == '÷' or sign == '//' or sign == '÷÷' or sign == '**' or sign == '××' or sign == '%':
    if operator == '+':
      print(">>> Output = ",val + val2)
    elif operator == '-':
      print(">>> Output = ",val - val2)
    elif operator == '*' or operator == '×':
      print(">>> Output = ",val * val2)
    elif operator == '/' or operator == '÷':
      print(">>> Output = ",val / val2)
    elif operator == '**' or operator == '××':
      print(">>> Output = ",val ** val2)
    elif operator == '//' or operator == '÷÷':
      print(">>> Output = ",val // val2)
    elif operator == '%':
      print(">>> Output = ",val % val2)
  else:
    print("\t<error> (wrong operator entered !)")
